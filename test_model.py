def plot_confusion_matrix(y_true, y_pred, save_path: Path):
    cm = confusion_matrix(y_true, y_pred)
    fig, ax = plt.subplots(figsize=(8, 6))
    sns.heatmap(cm, annot=True, fmt="d", cmap="Blues",
                xticklabels=CLASS_NAMES, yticklabels=CLASS_NAMES,
                linewidths=0.5, ax=ax)
    ax.set_title("StutterSense — Confusion Matrix", fontsize=14, pad=12)
    ax.set_xlabel("Predicted Label", fontsize=11)
    ax.set_ylabel("True Label",      fontsize=11)
    plt.tight_layout()
    fig.savefig(save_path, dpi=150)
    plt.close(fig)
    log.info("Confusion matrix saved → %s", save_path)


def save_classification_report(y_true, y_pred, save_path: Path):
    report = classification_report(y_true, y_pred,
                                   target_names=CLASS_NAMES,
                                   digits=4)
    log.info("\n%s", report)
    with open(save_path, "w") as f:
        f.write("StutterSense — Classification Report\n")
        f.write("=" * 50 + "\n")
        f.write(report)
    log.info("Classification report saved → %s", save_path)


