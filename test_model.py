# 9.  MAIN TRAINING PIPELINE
# ═══════════════════════════════════════════════════════════════════════════════

def train(args):
    # ── 9.1  Load data ────────────────────────────────────────────────────────
    X, y, label_encoder = load_tensor_dataset(args.data_file)

    # ── 9.2  Train / val / test split (70 / 15 / 15) ─────────────────────────
    X_tv, X_test, y_tv, y_test = train_test_split(
        X, y, test_size=0.15, random_state=42, stratify=y)
    X_train, X_val, y_train, y_val = train_test_split(
        X_tv, y_tv, test_size=0.1765, random_state=42, stratify=y_tv)
    # 0.1765 of 0.85 ≈ 15 % of total

    log.info("Split sizes — train: %d  val: %d  test: %d",
             len(X_train), len(X_val), len(X_test))

    # ── 9.3  StandardScaler (fit on training set only) ────────────────────────
    scaler_path = OUTPUT_DIR / "scaler.pkl"
    scaler      = fit_and_save_scaler(X_train, scaler_path)
    X_train     = apply_scaler(X_train, scaler)
    X_val       = apply_scaler(X_val,   scaler)
    X_test      = apply_scaler(X_test,  scaler)

    # ── 9.4  Augment minority classes (tensor-level, preserves temporal structure)
    if args.augment:
        X_train, y_train = augment_minority(
            X_train, y_train,
            target_ratio=0.65,
            noise_std=0.04,
            time_mask_max=12,
        )
    elif args.smote:
        try:
            X_train, y_train = apply_smote(X_train, y_train)
        except Exception as exc:
            log.warning("SMOTE failed (%s) — proceeding without resampling.", exc)

    # ── 9.5  Build model ──────────────────────────────────────────────────────
    n_features = X_train.shape[-1]          # 39 
    seq_len    = X_train.shape[1]           # 128

    model = build_transformer_model(
        seq_len    = seq_len,
        n_features = n_features,
        n_classes  = N_CLASSES,
        d_model    = args.d_model,
        num_heads  = 4,
        ff_dim     = args.ff_dim,
        num_blocks = 3,
        dropout    = args.dropout,
    )
    model.summary(print_fn=log.info)

    # ── 9.6  Compile with Focal Loss + gradient clipping ──────────────────
    model.compile(
        optimizer = keras.optimizers.Adam(
            learning_rate=args.lr,
            clipnorm=1.0),
        loss      = FocalLoss(gamma=2.0, alpha=0.25),   # gamma=2 focuses harder on minorities
        metrics   = ["accuracy"],
    )

    # ── 9.7  Callbacks ────────────────────────────────────────────────────────
    cb_list = [
        callbacks.EarlyStopping(monitor="val_accuracy", patience=10,
                                 restore_best_weights=True,
                                 mode="max", verbose=1),
        callbacks.ReduceLROnPlateau(monitor="val_loss", factor=0.5,
                                    patience=4, min_lr=1e-7, verbose=1),
        callbacks.ModelCheckpoint(filepath=str(OUTPUT_DIR / "transformer_model.keras"),
                                   monitor="val_accuracy", save_best_only=True,
                                   mode="max", verbose=0),
    ]

    # ── 9.8  Class weights — stronger penalty on minorities ─────────────────
    counts    = np.bincount(y_train, minlength=N_CLASSES).astype(float)
    majority  = counts.max()
    # Square-root smoothing: less aggressive than inverse, avoids exploding weights
    class_wts = {i: np.sqrt(majority / max(c, 1)) for i, c in enumerate(counts)}
    log.info("Class weights: %s",
             {CLASS_NAMES[i]: f"{w:.2f}" for i, w in class_wts.items()})

    # ── 9.9  Train ────────────────────────────────────────────────────────────
    history = model.fit(
        X_train, y_train,
        validation_data = (X_val, y_val),
        epochs          = args.epochs,
        batch_size      = args.batch,
        class_weight    = class_wts,
        callbacks       = cb_list,
        verbose         = 1,
    )

    # ── 9.10  Evaluate ────────────────────────────────────────────────────────
    log.info("── Test-set evaluation ──────────────────────────────")
    y_prob  = model.predict(X_test, verbose=0)
    y_pred  = np.argmax(y_prob, axis=-1)

    save_classification_report(y_test, y_pred,
                                OUTPUT_DIR / "classification_report.txt")
    plot_confusion_matrix(y_test, y_pred,
                          OUTPUT_DIR / "confusion_matrix.png")

    # ── 9.11  Training curve ──────────────────────────────────────────────────
    _plot_training_curves(history, OUTPUT_DIR / "training_curves.png")

    log.info("All artefacts saved to  %s/", OUTPUT_DIR)
    return model, scaler


def _plot_training_curves(history, save_path: Path):
    fig, axes = plt.subplots(1, 2, figsize=(12, 4))
    for ax, metric, title in zip(axes,
                                  ["loss", "accuracy"],
                                  ["Focal Loss", "Accuracy"]):
        ax.plot(history.history[metric],          label="train")
        ax.plot(history.history[f"val_{metric}"], label="val")
        ax.set_title(title)
        ax.set_xlabel("Epoch")
        ax.legend()
    plt.tight_layout()
    fig.savefig(save_path, dpi=120)
    plt.close(fig)
    log.info("Training curves saved → %s", save_path)


