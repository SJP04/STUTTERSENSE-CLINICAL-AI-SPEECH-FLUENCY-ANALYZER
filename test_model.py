def parse_args(argv=None):
    p = argparse.ArgumentParser(
        description="StutterSense — Transformer Training Pipeline",
        formatter_class=argparse.ArgumentDefaultsHelpFormatter,
    )
    # paths
    p.add_argument("--data_file", default="./sep28k_tensors.npz",
                   help="Path to the .npz file from build_wav_dataset.py")
    # training hyper-params
    p.add_argument("--epochs",    type=int,   default=80,
                   help="Max epochs (early stopping usually triggers earlier)")
    p.add_argument("--batch",     type=int,   default=32)
    p.add_argument("--lr",        type=float, default=1e-4)
    p.add_argument("--dropout",   type=float, default=0.2)
    p.add_argument("--d_model",   type=int,   default=256,
                   help="Transformer embedding dimension")
    p.add_argument("--ff_dim",    type=int,   default=512,
                   help="Feed-forward inner dimension")
    # flags
    p.add_argument("--augment",    action="store_true", default=True,
                   help="Augment minority classes with noise+time-masking (recommended)")
    p.add_argument("--no_augment", dest="augment", action="store_false")
    p.add_argument("--smote",      action="store_true", default=False,
                   help="Apply SMOTE (not recommended for temporal data — use --augment)")
    p.add_argument("--no_smote",   dest="smote", action="store_false")
    # inference demo
    p.add_argument("--infer",     metavar="AUDIO_FILE",
                   help="Run live inference on an audio file (requires librosa)")
    return p.parse_args(argv)


