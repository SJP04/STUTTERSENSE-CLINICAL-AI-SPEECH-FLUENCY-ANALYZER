# 8.  LIVE AUDIO INFERENCE  (COMPLETED: 39-dim sliding window)
# ═══════════════════════════════════════════════════════════════════════════════

def extract_live_features(audio: np.ndarray, sr: int = SR,
                           n_mfcc: int = N_MFCC) -> np.ndarray:
    """
    Extract 39-dim feature vector from a raw audio waveform array:
      13 MFCCs + 13 Deltas + 13 Delta-Deltas
    
    This completes the pipeline so live inference perfectly matches 
    the 39-dimensional Transformer training logic.
    """
    if not LIBROSA_AVAILABLE:
        raise RuntimeError("librosa is required for live inference.  pip install librosa")
    
    hop_length = 512
    
    # 1. Base 13 MFCCs
    mfcc_base = librosa.feature.mfcc(y=audio, sr=sr, n_mfcc=n_mfcc, hop_length=hop_length).T  # (T, 13)
    
    # 2. Compute Deltas & Delta-Deltas to match training trajectory
    delta  = librosa.feature.delta(mfcc_base.T, order=1).T    # (T, 13)
    delta2 = librosa.feature.delta(mfcc_base.T, order=2).T    # (T, 13)
    
    # 3. Combine into the final 39 features expected by the Transformer
    feats = np.concatenate([mfcc_base, delta, delta2], axis=-1)  # (T, 39)
    
    return feats


class LiveInferenceEngine:
    """
    Loads a trained StutterSense model + scaler and classifies 3-second
    sliding windows of live audio.

    For live use the model must accept (batch, SEQ_LEN, LIVE_FEATURES).
    """

    def __init__(self, model_path: str, scaler_path: str):
        self.model  = keras.models.load_model(model_path,
                                              custom_objects={"FocalLoss": FocalLoss})
        with open(scaler_path, "rb") as f:
            self.scaler = pickle.load(f)
        log.info("LiveInferenceEngine ready.")

    def predict_window(self, audio: np.ndarray,
                       sr: int = SR) -> tuple:
        """Return (predicted_class_name, confidence, probabilities_array)."""
        feats  = extract_live_features(audio, sr=sr)
        feats  = pad_or_truncate(feats)                              # (SEQ_LEN, 39)
        # scale
        F = feats.shape[-1]
        feats  = self.scaler.transform(feats.reshape(-1, F)).reshape(1, SEQ_LEN, F)
        probs  = self.model.predict(feats, verbose=0)[0]
        idx    = int(np.argmax(probs))
        return CLASS_NAMES[idx], float(probs[idx]), probs

    def run_sliding_window(self, audio: np.ndarray,
                           sr: int = SR,
                           window_sec: float = WINDOW_SEC,
                           hop_sec:    float = HOP_SEC) -> list:
        """
        Slide a 3-second window over a longer recording.
        Returns a list of dicts: {start_sec, end_sec, label, confidence}.
        """
        win_samples = int(window_sec * sr)
        hop_samples = int(hop_sec    * sr)
        results = []
        for start in range(0, len(audio) - win_samples + 1, hop_samples):
            window = audio[start: start + win_samples]
            label, conf, _ = self.predict_window(window, sr=sr)
            results.append({
                "start_sec":  round(start / sr, 2),
                "end_sec":    round((start + win_samples) / sr, 2),
                "label":      label,
                "confidence": round(conf, 4),
            })
        return results


