# 4.  TRANSFORMER ENCODER BLOCK
# ═══════════════════════════════════════════════════════════════════════════════

def transformer_encoder_block(x, d_model: int, num_heads: int,
                               ff_dim: int, dropout: float = 0.1):
    """
    One Transformer Encoder block:
      Multi-Head Self-Attention → Add & Norm → FFN → Add & Norm
    """
    # ── Multi-Head Self-Attention ─────────────────────────────────────────────
    attn_out = layers.MultiHeadAttention(
        num_heads=num_heads, key_dim=d_model // num_heads,
        dropout=dropout, name=None)(x, x)
    x = layers.LayerNormalization(epsilon=1e-6)(x + attn_out)

    # ── Feed-Forward Network ──────────────────────────────────────────────────
    ffn_out = layers.Dense(ff_dim, activation="relu")(x)
    ffn_out = layers.Dropout(dropout)(ffn_out)
    ffn_out = layers.Dense(d_model)(ffn_out)
    x = layers.LayerNormalization(epsilon=1e-6)(x + ffn_out)

    return x


def build_transformer_model(seq_len: int, n_features: int,
                             n_classes: int,
                             d_model: int = 256,
                             num_heads: int = 4,
                             ff_dim: int = 512,
                             num_blocks: int = 3,
                             dropout: float = 0.2,
                             l2_reg: float = 1e-4) -> Model:
    """
    Upgraded Transformer Encoder model for stuttering classification.

    Architecture (accuracy-optimised)
    ───────────────────────────────────
      Input projection  → d_model=256
      Learned positional encoding
      3 × Transformer Encoder Blocks (4-head MHA + FFN + LayerNorm)
      Global Average Pooling
      Dense(256, gelu) + Dropout(0.3)
      Dense(128, relu) + Dropout(0.2)
      Dense(n_classes, softmax)
    """
    inp = keras.Input(shape=(seq_len, n_features), name="mfcc_input")

    # ── linear projection to d_model ──────────────────────────────────────
    x = layers.Dense(d_model, name="input_projection")(inp)

    # ── learned positional encoding ──────────────────────────────────────
    positions = tf.range(start=0, limit=seq_len, delta=1)
    pos_emb   = layers.Embedding(input_dim=seq_len, output_dim=d_model,
                                  name="pos_embedding")(positions)
    x = x + pos_emb
    x = layers.Dropout(dropout)(x)

    # ── 3 Transformer Encoder Blocks ────────────────────────────────────
    for i in range(num_blocks):
        x = transformer_encoder_block(x, d_model=d_model,
                                      num_heads=num_heads,
                                      ff_dim=ff_dim,
                                      dropout=dropout)

    # ── Global Average Pooling ───────────────────────────────────────────
    x = layers.GlobalAveragePooling1D(name="global_avg_pool")(x)

    # ── Deeper classification head for improved minority class recall ───────
    x = layers.Dense(256, activation="gelu", name="clf_dense_1")(x)
    x = layers.Dropout(0.3)(x)
    x = layers.Dense(128, activation="relu", name="clf_dense_2")(x)
    x = layers.Dropout(0.2)(x)
    out = layers.Dense(n_classes, activation="softmax", name="predictions")(x)

    return Model(inputs=inp, outputs=out, name="StutterSense_Transformer_v2")


