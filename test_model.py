# 3.  FOCAL LOSS
# ═══════════════════════════════════════════════════════════════════════════════

class FocalLoss(keras.losses.Loss):
    """
    Focal Loss for multi-class classification.
      FL(p_t) = -α_t · (1 − p_t)^γ · log(p_t)

    γ=1.5  — moderate focus on hard examples
    α=0.25 — mild down-weighting of the dominant NoStutter class
    """
    def __init__(self, gamma: float = 1.5, alpha: float = 0.25,
                 name: str = "focal_loss", **kwargs):
        # Pop 'reduction' and any other Keras-internal keys — do NOT forward them.
        # Different Keras versions use different Reduction enum values, so the
        # safest approach is to discard it and let Keras use its own default.
        kwargs.pop('reduction', None)
        super().__init__(name=name, **kwargs)
        self.gamma = gamma
        self.alpha = alpha

    def call(self, y_true, y_pred):
        y_true    = tf.cast(y_true, tf.int32)
        y_pred    = tf.clip_by_value(y_pred, 1e-7, 1.0 - 1e-7)
        ce        = -tf.math.log(y_pred)                          # (B, C)
        one_hot   = tf.one_hot(y_true, depth=N_CLASSES)           # (B, C)
        p_t       = tf.reduce_sum(y_pred * one_hot, axis=-1)      # (B,)
        focal_w   = self.alpha * tf.pow(1.0 - p_t, self.gamma)    # (B,)
        loss      = focal_w * tf.reduce_sum(ce * one_hot, axis=-1)
        return tf.reduce_mean(loss)

    def get_config(self):
        base = super().get_config()
        base.update({"gamma": self.gamma, "alpha": self.alpha})
        return base


