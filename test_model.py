# 1.  FEATURE EXTRACTION — 39-dim MFCC trajectory
# ═══════════════════════════════════════════════════════════════════════════════

def compute_39dim_features(mfcc_matrix: np.ndarray) -> np.ndarray:
    """
    Given a (time, 13) MFCC matrix, append Δ and ΔΔ along the feature axis.
    Returns shape (time, 39).
    """
    delta     = librosa.feature.delta(mfcc_matrix.T, order=1).T   # (time, 13)
    delta2    = librosa.feature.delta(mfcc_matrix.T, order=2).T   # (time, 13)
    return np.concatenate([mfcc_matrix, delta, delta2], axis=-1)   # (time, 39)


def pad_or_truncate(arr: np.ndarray, seq_len: int = SEQ_LEN) -> np.ndarray:
    """Pad with zeros or truncate to a fixed sequence length."""
    t = arr.shape[0]
    if t >= seq_len:
        return arr[:seq_len]
    pad = np.zeros((seq_len - t, arr.shape[1]), dtype=arr.dtype)
    return np.concatenate([arr, pad], axis=0)


