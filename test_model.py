# 6.  SCALER FIT / PERSIST
# ═══════════════════════════════════════════════════════════════════════════════

def fit_and_save_scaler(X_train: np.ndarray, scaler_path: Path) -> StandardScaler:
    """
    Fit a StandardScaler on the training set (frame-wise across all time steps)
    and persist it for live inference.
    """
    N, T, F = X_train.shape
    X_flat = X_train.reshape(-1, F)
    scaler = StandardScaler()
    scaler.fit(X_flat)
    with open(scaler_path, "wb") as f:
        pickle.dump(scaler, f)
    log.info("Scaler saved → %s", scaler_path)
    return scaler


def apply_scaler(X: np.ndarray, scaler: StandardScaler) -> np.ndarray:
    N, T, F = X.shape
    return scaler.transform(X.reshape(-1, F)).reshape(N, T, F).astype(np.float32)


