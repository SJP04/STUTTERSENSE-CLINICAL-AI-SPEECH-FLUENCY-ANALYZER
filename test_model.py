# 5.  SMOTE HELPER  (operates on flattened sequences)
# ═══════════════════════════════════════════════════════════════════════════════

def apply_smote(X: np.ndarray, y: np.ndarray) -> tuple:
    """
    Flatten (N, T, F) → (N, T*F), apply SMOTE, reshape back to (N', T, F).
    NOTE: Disabled by default — SMOTE on temporal data destroys the frame
    ordering that the Transformer depends on.  Use augment_minority() instead.
    """
    original_shape = X.shape[1:]            # (T, F)
    N, T, F = X.shape
    X_flat = X.reshape(N, T * F)

    class_counts = np.bincount(y)
    log.info("Class distribution before SMOTE: %s", dict(zip(range(N_CLASSES), class_counts)))

    smote = SMOTE(random_state=42, k_neighbors=min(5, np.min(class_counts) - 1))
    X_res, y_res = smote.fit_resample(X_flat, y)

    class_counts_after = np.bincount(y_res)
    log.info("Class distribution after  SMOTE: %s", dict(zip(range(N_CLASSES), class_counts_after)))

    return X_res.reshape(-1, T, F), y_res


def augment_minority(X: np.ndarray, y: np.ndarray,
                     target_ratio: float = 0.6,
                     noise_std: float = 0.05,
                     time_mask_max: int = 15) -> tuple:
    """
    Augment minority stutter classes with realistic tensor-level perturbations:
      • Gaussian noise injection  (simulates microphone variance)
      • Random time-frame masking (simulates dropout / codec loss)

    Minority classes are up-sampled until they reach `target_ratio` of the
    majority class count.  This preserves temporal structure completely.
    """
    counts       = np.bincount(y, minlength=N_CLASSES)
    majority_n   = int(counts.max())
    target_n     = int(majority_n * target_ratio)

    log.info("Class counts before augmentation: %s", dict(zip(CLASS_NAMES, counts)))

    X_aug_list = [X]
    y_aug_list = [y]

    rng = np.random.RandomState(42)

    for cls_idx in range(N_CLASSES):
        cls_mask = (y == cls_idx)
        cls_count = int(cls_mask.sum())
        if cls_count >= target_n:
            continue   # already enough samples — no augmentation needed

        X_cls = X[cls_mask]           # (C, 128, 39)
        needed = target_n - cls_count
        log.info("  Augmenting class %s: +%d samples", CLASS_NAMES[cls_idx], needed)

        aug_indices = rng.randint(0, cls_count, size=needed)
        X_src       = X_cls[aug_indices].copy()   # (needed, 128, 39)

        # 1. Gaussian noise
        noise = rng.normal(0, noise_std, X_src.shape).astype(np.float32)
        X_src = X_src + noise

        # 2. Random time masking — zero out up to time_mask_max consecutive frames
        for sample in X_src:
            mask_len   = rng.randint(1, time_mask_max + 1)
            mask_start = rng.randint(0, max(1, SEQ_LEN - mask_len))
            sample[mask_start: mask_start + mask_len, :] = 0.0

        X_aug_list.append(X_src)
        y_aug_list.append(np.full(needed, cls_idx, dtype=y.dtype))

    X_out = np.concatenate(X_aug_list, axis=0)
    y_out = np.concatenate(y_aug_list, axis=0)

    # Shuffle so augmented samples are interspersed with real ones
    perm  = rng.permutation(len(X_out))
    X_out, y_out = X_out[perm], y_out[perm]

    counts_after = np.bincount(y_out, minlength=N_CLASSES)
    log.info("Class counts after  augmentation: %s", dict(zip(CLASS_NAMES, counts_after)))
    return X_out, y_out


